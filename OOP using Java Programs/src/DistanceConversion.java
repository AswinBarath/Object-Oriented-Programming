
public class DistanceConversion {

	public static void main(String[] args) {
		// Program to convert meters to kilometers and display the output in kilometers
		int i = 2000;
		System.out.println("the distance is= " + i + " meters");
		int km = i / 1000;
		System.out.println("the distance in kilometer is= " + km);

	}

}
